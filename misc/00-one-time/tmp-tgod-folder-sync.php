function sanatizeCN($string) {
    $string = str_replace('《','-',$string);
    $string = str_replace('》','-',$string);
    $string = str_replace('(','-',$string);
    $string = str_replace(')','-',$string);
    $string = str_replace('，','-',$string);
    $string = str_replace('、','-',$string);
    $string = str_replace('&','-',$string);

    return $string;
}
function my_mb_ucfirst($str) {
    $new_str = '';
    $do_it = true;
    for ($i = 0; $i < strlen($str); $i++) {
        if(ord($str[$i]) >=97 && ord($str[$i]) <=122) {
            if($do_it) {
                $new_str[$i] = strtoupper($str[$i]);
                $do_it = false;
            }
            else {
                $new_str[$i] = $str[$i];
            }
        }
        else {
            $new_str[$i] = $str[$i];
        }

    }
    return $new_str;
}